while True:
    f_1 = float(input("Please enter the first number here: "))
    op = input("Please enter the operator that you need here: ")
    f_2 = float(input("Please enter the second number here: "))

    if op == "+":
        print(f_1 + f_2)

    elif op == "-":
        print(f_1 - f_2)

    elif op == "*":
        print(f_1 * f_2)

    elif op == "/":
        print(f_1 / f_2)
    
    else:
        print("Invalid sintax")
        break

    break

    
    

